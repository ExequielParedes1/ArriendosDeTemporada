    private void btnRegistarUsuario1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarUsuario1ActionPerformed
        usuario usernew = new usuario();

        encryptPass ep = new encryptPass();
        boolean validado = false;

        if (txtRut1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por Favor Ingrese un RUT valido");
        }else if (txtNombre1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre valido");
        }else if (txtPass1.getText().length() <= 3) {
            JOptionPane.showMessageDialog(null, "Ingrese una contraseña mas larga para mayor seguridad");
        }else if (txtPass1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por Favor Ingrese una contraseña");
        }else if (cmbRol1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un ROL de usuario");
        }else if (txtAppaterno1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un apellido Paterno");
        }else if (txtApmaterno1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un apellido Materno");
        }else if (txtEmail1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingresa un correo valido");
        }else if(cmbEstadoUsuario1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un estado");
        }else{

            try {
                usernew.setRut_usuario(txtRut1.getText());
                usernew.setNombre_usuario(txtNombre1.getText());
                usernew.setContrasenia(ep.encriptarPass(txtPass1.getText()));
                usernew.setRol(cmbRol1.getSelectedItem().toString());
                usernew.setApellido_p(txtAppaterno1.getText());
                usernew.setApellido_m(txtApmaterno1.getText());
                usernew.setEmail(txtEmail1.getText());
                usernew.setEstado_usuario(cmbEstadoUsuario1.getSelectedItem().toString());

                validado = userdto.ingresarUsuario(usernew);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

        if (validado == true) {
            JOptionPane.showMessageDialog(null, "Ingresado con exito");
            txtRut1.setText("");
            txtNombre1.setText("");
            txtPass1.setText("");
            cmbRol1.setSelectedIndex(0);
            txtAppaterno1.setText("");
            txtApmaterno1.setText("");
            txtEmail1.setText("");
            cmbEstadoUsuario1.setSelectedIndex(0);
            limpiaTabla(tablaUsuario);
            listarUsuarios();
        }else{
            JOptionPane.showMessageDialog(null, "Error al ingresar");
        }
    }//GEN-LAST:event_btnRegistarUsuario1ActionPerformed

