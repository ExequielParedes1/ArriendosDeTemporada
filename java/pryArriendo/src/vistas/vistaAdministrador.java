    private void btnRegistroDeptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroDeptoActionPerformed
        departamento dep = new departamento();
        boolean pasa = false;
        
         if (txtNumDepto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero de Dpto");
        }else if(txtDireccionDpto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese una direccion valida");
        }else if(txtDescrip.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor Escriba una Descripcion");
        }else if (cmbRegion.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione una region valida");
        }else if(Integer.parseInt(txtValorDiario.getValue().toString()) <= 0){
            JOptionPane.showMessageDialog(null, "Ingrese un valor");
        }else{

            try{
                dep.setNum_depto(txtNumDepto.getText());
                dep.setDireccion(txtDireccionDpto.getText());
                dep.setDescripcion(txtDescrip.getText());
                dep.setRegion(cmbRegion.getSelectedItem().toString());
                dep.setEstado("DISPONIBLE");
                dep.setValor_diario(Integer.parseInt(txtValorDiario.getValue().toString()));
                
                pasa = deptoDTO.ingresarDepto(dep);
                
            }catch(Exception  e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
         
         if (pasa == true) {
            JOptionPane.showMessageDialog(null, "Ingresado con exito");
            txtNumDepto.setText("");
            txtDireccionDpto.setText("");
            txtDescrip.setText("");
            cmbRegion.setSelectedIndex(0);
            txtValorDiario.setValue(0);

            limpiaTabla(tablaDepto);
            listarDepto();
        }else{
            JOptionPane.showMessageDialog(null, "Error al ingresar");
        }
        
    }//GEN-LAST:event_btnRegistroDeptoActionPerformed

