    private void btnRegistraServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistraServicioActionPerformed
        servicio se = new servicio();
        boolean pasa = false;
        
        java.sql.Date llegada = new java.sql.Date(txtLlegada.getDate().getTime());
        java.sql.Date salida = new java.sql.Date(txtInicio.getDate().getTime());
        if(txtNomServicio.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor escriba nombre al servicio");  
        }else if(cmbTipoServicio.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un servicio");
        }else if(txtPuntoReunion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor escriba punto de reunion");  
        }else if(txtInicio.getDate()==null){
            JOptionPane.showMessageDialog(null, "Por favor seleccione fecha salida","error",JOptionPane.ERROR_MESSAGE);
        }else if(txtLlegada.getDate()==null){
            JOptionPane.showMessageDialog(null, "Por favor seleccione fecha llegada","error",JOptionPane.ERROR_MESSAGE);      
        }else if (Integer.parseInt(txtValor.getValue().toString()) <= 0) {
            JOptionPane.showMessageDialog(null, "Ingrese valor de tour");
        }else{

            try{

                se.setNombre(txtNomServicio.getText());
                se.setTipo(cmbTipoServicio.getSelectedItem().toString());
                se.setHora_ini(salida);
                se.setHora_fin(llegada);
                se.setPunto_reunion(txtPuntoReunion.getText());
                se.setValor(Integer.parseInt(txtValor.getValue().toString()));
                se.setRecorrido(txtRecorrido.getText());
                se.setVehiculo(txtVehiculo.getText());
                se.setChofer(txtxConductor.getText());
                se.setEstado_servicio("DISPONIBLE");
                
                
//                System.out.println(txtRecorrido.getText());
//                System.out.println(txtPuntoReunion.getText());
//                System.out.println(salida);
//                System.out.println(llegada);
//                System.out.println(Integer.parseInt(txtValor.getValue().toString()));

                
                pasa = servDTO.ingresarServicio(se);
                
            }catch(Exception  e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
         
         if (pasa == true) {
            JOptionPane.showMessageDialog(null, "Ingresado con exito");
            
            txtNomServicio.setText("");
            cmbTipoServicio.setSelectedIndex(0);
            txtInicio.setCalendar(null);
            txtLlegada.setCalendar(null);
            txtPuntoReunion.setText("");
            txtValor.setValue(0);
            txtRecorrido.setText("");
            txtVehiculo.setText("");
            txtxConductor.setText("");

            limpiaTabla(tablaServicios);
            listarServicios();
        }else{
            JOptionPane.showMessageDialog(null, "Error al ingresar");
        }
        
    }//GEN-LAST:event_btnRegistraServicioActionPerformed

